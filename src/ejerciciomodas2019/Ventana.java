    private void jComboBoxOrdenacionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxOrdenacionItemStateChanged
        int numeroColumna=0;
        if(jComboBoxOrdenacion.getSelectedIndex()==0){ // Para el Código
            numeroColumna = 0;
            
        }else if(jComboBoxOrdenacion.getSelectedIndex()==1){ //Para el estilo
            numeroColumna= 1;
        }else if(jComboBoxOrdenacion.getSelectedIndex()==2){ //Para el proveedor
            numeroColumna= 3;
        }
        TableRowSorter<TableModel> sorter = new TableRowSorter(this.tablaListado.getModel());
        this.tablaListado.setRowSorter(sorter);
        ArrayList<RowSorter.SortKey> listaOrdenacion = new ArrayList<>();
        listaOrdenacion.add(new RowSorter.SortKey(numeroColumna, SortOrder.ASCENDING));
        sorter.setSortKeys(listaOrdenacion);
        sorter.sort();
    }//GEN-LAST:event_jComboBoxOrdenacionItemStateChanged


	private void comprobar() throws MiExcepcion {
		String codigo = jTextField1.getText();
		TipoPrenda tipoSeleccionado = (TipoPrenda)jComboBox1.getSelectedItem();
		Proveedor proveedorSeleccionado = (Proveedor)jComboBox2.getSelectedItem();
		if (!Pattern.matches("[a-zA-Z]{3}-[0-9]{2}", codigo)) {
			throw new MiExcepcion(jTextField1, "El codigo debe estar compuesto por 3 letras seguidas de un guion y dos numeros,");
		}
		if (tipoSeleccionado == null)
		{
			throw new MiExcepcion(jTextField1, "Debes seleccionar el tipo");
		}
		if (proveedorSeleccionado == null)
		{
			throw new MiExcepcion(jTextField1, "Debes seleccionar el proveedor de la prenda");
		}
	}

	private void agragar() 
	{
		String codigo = jTextField1.getText();
		TipoPrenda tipoSeleccionado = (TipoPrenda)jComboBox1.getSelectedItem();
		Proveedor proveedorSeleccionado = (Proveedor)jComboBox2.getSelectedItem();
		boolean especial =  jRadioButton1.isSelected();
		this.gestora.agregarPrenda(new Prenda(codigo.toUpperCase(), tipoSeleccionado, especial, proveedorSeleccionado));
		JOptionPane.showMessageDialog(this, "Nueva prenda almacenada");
	}

	private void limpiar() 
	{
		jTextField1.setText("");
		jComboBox1.setSelectedItem(null);
		jComboBox2.setSelectedItem(null);
		jRadioButton1.setSelected(false);
		jRadioButton2.setSelected(true);
	}


    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        for(int idx: tablaListado.getSelectedRows()){
            String codigo= (String) this.tablaListado.getModel().getValueAt(idx, 0);
            gestor.eliminarPrenda(codigo);
        }
        actualizarPaneles();
    }//GEN-LAST:event_bEliminarActionPerformed

    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        int idx = this.tablaListado.getSelectedRow();
        String codigo = (String) this.tablaListado.getModel().getValueAt(idx, 0);
        Prenda prenda = gestor.devolverPrenda(codigo);
        VentanaModificacion mod= new VentanaModificacion(this, true, prenda, gestor);
        mod.setVisible(true);
    }//GEN-LAST:event_bModificarActionPerformed


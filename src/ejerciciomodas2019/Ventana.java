    private void bBorrarSeleccionadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBorrarSeleccionadosActionPerformed
        int[] indices = jtListado.getSelectedRows();
        Prenda[] lasPrendas = this.gestora.devolverPrendasOrdenado();
        
        for(int indice : indices)
            gestora.eliminarPrenda(lasPrendas[indice].getCodigo());
        
        actualizarTabla();
    }//GEN-LAST:event_bBorrarSeleccionadosActionPerformed


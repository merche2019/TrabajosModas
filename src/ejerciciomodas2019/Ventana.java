    protected void comprobar() throws MiExcepcion {
        String codigo = ctCodigo.getText();
        Object objetoTipo = cbTipo.getSelectedItem();
        Object objetoProveedor = cbProveedor.getSelectedItem();
        
        if(!codigo.matches("(?i)[A-Z]{3}-\\d{2}")) {
            throw new MiExcepcion(ctCodigo, "Código de prenda no válido.");
        }
        if(objetoTipo == null) {
            throw new MiExcepcion(cbTipo, "Debe seleccionar un Tipo de prenda.");
        }
        if(objetoProveedor == null) {
            throw new MiExcepcion(cbProveedor, "Debe seleccionar un Proveedor.");
        }
        if(rGrupoTallaEspecial.getSelection() == null) {
            throw new MiExcepcion(jrTallaEspecial, "Debe seleccionar si es o no Talla Especial.");
        }
        if(gestora.existePrenda(new Prenda(codigo, TipoPrenda.CLASICA, true, null))) {
            
            throw new MiExcepcion(bAceptar, "La prenda introducida ya existe.");
        }
    }

    private void procesar() {
        String codigo = ctCodigo.getText();
        TipoPrenda tipo = (TipoPrenda) cbTipo.getSelectedItem();
        boolean esEspecial = jrTallaEspecial.isSelected();
        Proveedor proveedor = gestora.devolverProveedor(cbProveedor.getSelectedItem().toString());
        
        if(gestora.agregarPrenda(new Prenda(codigo, tipo, esEspecial, proveedor))) {
            javax.swing.JOptionPane.showMessageDialog(this, "La prenda se ha añadido correctamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Algo malo ha pasado.");
        }
    }

    public static void limpiarPanel(javax.swing.JPanel panel) {
        java.awt.Component[] componentes = panel.getComponents();
        
        for(java.awt.Component unComponente : componentes) {
            if(unComponente instanceof javax.swing.JTextField) {
                ((javax.swing.JTextField) unComponente).setText(null);
            }
            else if(unComponente instanceof javax.swing.JComboBox) {
                ((javax.swing.JComboBox) unComponente).setSelectedItem(null);
            }
            else if(unComponente instanceof javax.swing.JPanel) {
                ((javax.swing.JPanel) unComponente).setBorder(
                            javax.swing.BorderFactory.createLineBorder(java.awt.Color.YELLOW)
                );
            }
        }
    }
    
    private void actualizarTabla() {
        jtListado.setModel(new ModeloTabla());
    }

    private void limpiar() {
        limpiarPanel(jpNuevaPrenda);
        rGrupoTallaEspecial.clearSelection();
    }

        private void comprobar() throws MiExcepcion{
            if(cbTipo.getSelectedItem().equals(null)){
                throw new MiExcepcion(cbTipo, "Debe usted seleccionar un tipo de prenda");
            }
            
            if(cbProveedorPrenda.getSelectedItem().equals(null)){
                throw new MiExcepcion(cbProveedorPrenda, "Debe usted seleccionar un proveedor");
            }
            
            String codigo = ctCodigo.toString();
            if(!m.codigoCorrecto(codigo)){
                throw new MiExcepcion(ctCodigo, "El codigo debe incluir 3 letras y 2 digitos num√©ricos");
            }
            
            if(gb.isSelected(boSi.getModel())==false && gb.isSelected(boNo.getModel())==false){
                throw new MiExcepcion(gb, "Debe decidir si la talla es especial o no");
            }
            
//             if(gb.getSelection().equals(null)){
//                throw new MiExcepcion(gb, "Debe decidir si la talla es especial o no");
//            }
            
            
            
            TipoPrenda tipo=TipoPrenda.valueOf(cbTipo.getSelectedItem().toString());
            String codigoProvisional=ctCodigo.getText();
            Proveedor proveedor=(Proveedor)cbProveedorPrenda.getSelectedItem();
             boolean especial=gb.isSelected(boSi.getModel());
            
            if(m.containsValue(new Prenda(codigoProvisional, tipo, especial, proveedor))){
                throw new MiExcepcion(ctCodigo, "Esta prenda ya existe");
            }
    }

        private void procesar(){
         TipoPrenda tipo=TipoPrenda.valueOf(cbTipo.getSelectedItem().toString());
         String codigo=ctCodigo.getText();
         Proveedor proveedor=(Proveedor)cbProveedorPrenda.getSelectedItem();
         boolean especial=gb.isSelected(boSi.getModel());
         m.agregarPrenda(new Prenda(codigo, tipo, especial, proveedor));
    }

        private void limpiar(){
          boNo.setSelected(false);
          boSi.setSelected(false);
          ctCodigo.setText(null);
          cbTipo.setSelectedItem(null);  
          cbProveedorPrenda.setSelectedItem(null); 
            
    }  

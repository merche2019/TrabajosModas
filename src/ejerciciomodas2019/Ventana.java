    private void bAceptarPrendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarPrendaActionPerformed
        if(cbProveedorPrenda.getSelectedItem()== null){
            JOptionPane.showMessageDialog(this, "Debes seleccionar el proveedor de la prenda");
            return;
        }
        if(cbTipo.getSelectedItem()== null){
            JOptionPane.showMessageDialog(this, "Debes seleccionar el tipo de prenda");
            return;
        }
        if(ctCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Hay que introducir un cÃ³digo de prenda compuesto por 3 letras seguidas de un guiÃ³n y 2 dÃ­gitos numÃ©ricos");
            return;
        }
        Pattern patron = Pattern.compile("^[A-ZÃ]{3,3}-[0-9]{2,2}");
        Matcher m = patron.matcher(ctCodigo.getText().toUpperCase());
        if(!m.find()){
            JOptionPane.showMessageDialog(this, "Hay que introducir un cÃ³digo de prenda compuesto por 3 letras seguidas de un guiÃ³n y 2 dÃ­gitos numÃ©ricos");
            return;
        }
        
        if (!this.boSi.isSelected() && !this.boNo.isSelected()) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar si es o no talla especial");
        }
        Prenda prenda= new Prenda(ctCodigo.getText().toUpperCase(), (TipoPrenda)cbTipo.getSelectedItem(), this.boSi.isSelected(), gestor.devolverProveedor((String)cbProveedorPrenda.getSelectedItem()));
        gestor.agregarPrenda(prenda);
        JOptionPane.showMessageDialog(this, "Nueva prenda almacenada");
        this.actualizarPaneles();
    
    }//GEN-LAST:event_bAceptarPrendaActionPerformed


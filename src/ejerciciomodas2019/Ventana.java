        private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
		int[] numeroSeleccionados = jTable1.getSelectedRows();
		if(numeroSeleccionados.length == 0)
		{
			JOptionPane.showMessageDialog(this, "Debes elegir una prenda para modificar.");
		}else if (numeroSeleccionados.length == 1)
		{
			new VentanaModificar(this, false, this.gestora, numeroSeleccionados[0]).setVisible(true);
			
		}else
		{
			JOptionPane.showMessageDialog(this, "Debes seleccionar solo una prenda.");
		}
        }//GEN-LAST:event_jButton4ActionPerformed


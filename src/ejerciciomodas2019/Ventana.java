    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        for (int i : tablaListado.getSelectedRows()){
            String codigo = (String)this.tablaListado.getModel().getValueAt(i, 0);
            gestor.eliminarPrenda(codigo);
        }
        actualizarPaneles();
    }//GEN-LAST:event_bEliminarActionPerformed

    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        int i = this.tablaListado.getSelectedRow();
        String codigo = (String)this.tablaListado.getModel().getValueAt(i, 0);
        Prenda laPrenda = gestor.devolverPrenda(codigo);
        VentanaModificacion modificar = new VentanaModificacion(this, true, laPrenda, gestor);
        modificar.setVisible(true);
    }//GEN-LAST:event_bModificarActionPerformed

    private void cbOrdenarItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbOrdenarItemStateChanged
        int numeroColumna=0;
        if(cbOrdenar.getSelectedIndex()==0){ // Para el CÃ³digo
            numeroColumna = 0;
            
        }else if(cbOrdenar.getSelectedIndex()==1){ //Para el estilo
            numeroColumna= 1;
        }else if(cbOrdenar.getSelectedIndex()==2){ //Para el proveedor
            numeroColumna= 3;
        }
        TableRowSorter<TableModel> sorter = new TableRowSorter(this.tablaListado.getModel());
        this.tablaListado.setRowSorter(sorter);
        ArrayList<RowSorter.SortKey> listaOrdenacion = new ArrayList<>();
        listaOrdenacion.add(new RowSorter.SortKey(numeroColumna, SortOrder.ASCENDING));
        sorter.setSortKeys(listaOrdenacion);
        sorter.sort();
    }//GEN-LAST:event_cbOrdenarItemStateChanged


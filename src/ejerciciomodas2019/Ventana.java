    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try {
            comprobar();
            procesar();
            limpiar();
        } catch (MiExcepcion ex) {
            javax.swing.JComponent elComponente = ex.getComponente();
            JOptionPane.showMessageDialog(this, ex.getMessage());
            elComponente.requestFocus();
            
            if(elComponente instanceof javax.swing.JTextField) {
                ((javax.swing.JTextField) elComponente).selectAll();
            }
            else if(elComponente instanceof javax.swing.JComboBox) {
                ((javax.swing.JComboBox) elComponente).setSelectedItem(null);
            }
            else if(elComponente instanceof javax.swing.JRadioButton) {
                ((javax.swing.JPanel)
                    ((javax.swing.JRadioButton)
                        elComponente).getParent()).setBorder(
                            javax.swing.BorderFactory.createLineBorder(java.awt.Color.RED)
                        );
            }
        }
    }//GEN-LAST:event_bAceptarActionPerformed


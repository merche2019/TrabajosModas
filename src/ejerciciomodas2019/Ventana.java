    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        try {
            comprobar();
            procesar();
            limpiar(); 
        } catch(MiExcepcion ex) {
            javax.swing.JComponent elComponente = ex.getElComponente();
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
            elComponente.requestFocus();
            
            if(elComponente instanceof javax.swing.JTextField) {
                ((javax.swing.JTextField) elComponente).selectAll();
            } else if(elComponente instanceof javax.swing.JComboBox) {
                ((javax.swing.JComboBox) elComponente).getSelectedItem();            
            } else if(elComponente instanceof javax.swing.JRadioButton) {
                ((javax.swing.JRadioButton) elComponente).isSelected();
            }
        }
    
                
    }//GEN-LAST:event_jButtonAceptarActionPerformed


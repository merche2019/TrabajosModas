        private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
		int[] seleccionadas = jTable1.getSelectedRows();
		Prenda[] prendas = this.gestora.devolverLasPrendasOrdenado();
		for(int seleccion:seleccionadas)
		{
			this.gestora.eliminarPrenda(prendas[seleccion].getCodigo());
		}
		jTable1.setModel(new ModeloTabla());
        }//GEN-LAST:event_jButton3ActionPerformed

